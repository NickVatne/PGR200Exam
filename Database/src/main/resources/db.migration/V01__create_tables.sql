CREATE TABLE IF NOT EXISTS TASKMANAGER
(
  ID          serial NOT NULL,
  FIRST_USER  VARCHAR(50),
  SECOND_USER VARCHAR(50),
  THIRD_USER  VARCHAR(50),
  PRIMARY KEY (ID)
);

CREATE TABLE IF NOT EXISTS TASK
(
  ID             serial NOT NULL,
  TITLE          VARCHAR(100),
  DESCRIPTION    VARCHAR(200),
  STATUS         VARCHAR(50),
  TASKMANAGER_ID int,
  PRIMARY KEY (ID),
  FOREIGN KEY (TASKMANAGER_ID) REFERENCES TASKMANAGER (ID)
);